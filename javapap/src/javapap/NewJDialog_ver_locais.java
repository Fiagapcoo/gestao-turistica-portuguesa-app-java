    private void jButton_alterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_alterarActionPerformed
        try {
            String Reg = jComboBox_Reg.getSelectedItem().toString();
            String Cidade = jTextField_Cidade.getText();
            String Local = jTextField_Local.getText();

            String SQL1 = "UPDATE local SET Regiao = '" + Reg + "' WHERE ID = '" + Javapap.ID + "';";
            String SQL2 = "UPDATE local SET Cidades_Reg = '" + Cidade + "' WHERE ID = '" + Javapap.ID + "';";
            String SQL3 = "UPDATE local SET Local = '" + Local + "' WHERE ID = '" + Javapap.ID + "';";
            Javapap.expressao.executeUpdate(SQL1);
            Javapap.expressao.executeUpdate(SQL2);
            Javapap.expressao.executeUpdate(SQL3);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton_alterarActionPerformed

