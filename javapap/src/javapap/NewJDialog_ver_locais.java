    private void jButton_leftActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_leftActionPerformed
        try {
            if (!Javapap.resultado.isFirst()) {
                Javapap.resultado.previous();
                jTextField_Cidade.setText(Javapap.resultado.getString("Cidades_Reg"));
                jTextField_Local.setText(Javapap.resultado.getString("Local"));
                jComboBox_Reg.setSelectedItem(Javapap.resultado.getObject("Regiao"));
                jTextField_ID.setText(Javapap.resultado.getString("ID"));
                Javapap.ID = jTextField_ID.getText();
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao carregar base de dados" + ex);
        }
    }//GEN-LAST:event_jButton_leftActionPerformed

    private void jButton_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_updateActionPerformed
        jTextField_Cidade.setEnabled(false);
        jTextField_Local.setEnabled(false);
        jComboBox_Reg.setEnabled(false);
        jTextField_ID.setEnabled(false);

        try {
            Javapap.sql = "SELECT * FROM local";
            Javapap.resultado = Javapap.expressao.executeQuery(Javapap.sql);
            Javapap.resultado.next();
            jTextField_Cidade.setText(Javapap.resultado.getString("Cidades_Reg"));
            jTextField_Local.setText(Javapap.resultado.getString("Local"));
            jComboBox_Reg.setSelectedItem(Javapap.resultado.getObject("Regiao"));
            jTextField_ID.setText(Javapap.resultado.getString("ID"));
            Javapap.ID = jTextField_ID.getText();

        } catch (SQLException ex) {
            Logger.getLogger(NewJDialog_Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton_updateActionPerformed

